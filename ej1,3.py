# Aritmética
a = 10 + 5 * 2 - 8 / 4  # 18.0
b = (10 + 5) * (2 - 8) / 4  # -7.5

# Asignación
c = 10
c += 5  # 15
c *= 2  # 30
c /= 5  # 6.0

# Comparación
d = 10 > 5    # True
e = 10 != 10  # False
f = "abc" == "abc"  # True

